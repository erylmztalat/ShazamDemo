import UIKit

extension UIColor {
    static var primary: UIColor {
        return UIColor(named: "PrimaryColor")!
    }
    
    static var primaryLight: UIColor {
        return UIColor(named: "PrimaryLightColor")!
    }
    
    static var shazam: UIColor {
        return UIColor(named: "ShazamBtnColor")!
    }
    
    static var shazamLight: UIColor {
        return UIColor(named: "ShazamBtnLightColor")!
    }
}
